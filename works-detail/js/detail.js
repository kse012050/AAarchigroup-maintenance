$(document).ready(function(){
    let worksInfo = $.cookie('worksInfo').split(',');
    detailData[worksInfo[0]][worksInfo[1]][worksInfo[2]].img.map((i)=>{
        $('[data-img]').append(
            `<div class="swiper-slide" style="background-image: url(images/${worksInfo[0]}/${i})">` +
            '</div>'
        )
    })
    $('[data-title]').html(detailData[worksInfo[0]][worksInfo[1]][worksInfo[2]].title)
    $('[data-info] li').each(function(i){
        $(this).append(detailData[worksInfo[0]][worksInfo[1]][worksInfo[2]]['info'][i]);
    })

    if(!!detailData[worksInfo[0]][worksInfo[1]][worksInfo[2]].content){
        $('[data-content]').append(
            `<mark class="white">프로젝트 개요</mark>
                <p>
                    ${detailData[worksInfo[0]][worksInfo[1]][worksInfo[2]]['content']}        
                </p>
            `
        )
    }
    detailData[worksInfo[0]][worksInfo[1]].length
    relatedLength = detailData[worksInfo[0]][worksInfo[1]].length;
    let relatedArray = []
    let relatedResult = []
    let relatedIdx;
    for(let a = 0; a < relatedLength; a++){
        relatedArray.push(a);
    }
    relatedArray.splice(worksInfo[2] , 1);
    relatedArray.length < 4 ? (relatedIdx = relatedArray.length) : (relatedIdx = 4);
    relatedLength -= 1;
    for(let a = 0; a < relatedIdx; a++){
        const chosen = relatedArray.splice(Math.floor(Math.random() * ((relatedLength) - a)) , 1)[0];
        relatedResult.push(chosen);
        console.log(chosen);
    }
    for(let a = 0; a < relatedResult.length; a++){
        const idx = (relatedResult[a] + 1);
        const numb = (String(idx).length < 2 ? '0' + idx : idx);
        $('[data-related]').append(
            `<li>
                <a href="active-hotel.html" onclick="$.cookie('worksInfo' , '${worksInfo[0]},${worksInfo[1]},${relatedResult[a]}')">
                    <div class="imgBox" style="background-image: url('../worksDetailImages/${worksInfo[0]}${worksInfo[1] !== 'basic' ? ('-'+ worksInfo[1] + numb ): (numb)}.jpg');"></div>
                    <small>${detailData[worksInfo[0]][worksInfo[1]][relatedResult[a]].info[0]}</small>
                    <p>${detailData[worksInfo[0]][worksInfo[1]][relatedResult[a]].title}</p>
                </a>
            </li>`
        )
    }

    $.removeCookie('worksInfo');
})